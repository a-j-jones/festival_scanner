class Artist {
  constructor(id, name, images, startTime, endTime, stageName, stageDay, rank) {
      this.id = id;
      this.name = name;
      this.images = images;
      this.startTime = startTime;
      this.endTime = endTime;
      this.stageName = stageName;
      this.stageDay = stageDay;
      this.rank = rank;
  }
}